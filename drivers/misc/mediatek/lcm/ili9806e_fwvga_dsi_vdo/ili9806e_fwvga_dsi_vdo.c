static void lcm_init(void)
{
	SET_RESET_PIN(1);
	SET_RESET_PIN(0);
	MDELAY(10);
	SET_RESET_PIN(1);
	MDELAY(10);
	
	push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);
}

static void lcm_suspend(void)
{
	push_table(lcm_deep_sleep_in_setting, sizeof(lcm_deep_sleep_in_setting) / sizeof(struct LCM_setting_table), 1);
}

static void lcm_resume(void)
{
	push_table(lcm_sleep_out_setting, sizeof(lcm_sleep_out_setting) / sizeof(struct LCM_setting_table), 1);
}

static unsigned int lcm_compare_id(void)
{
	unsigned int array[4];
	unsigned short device_id;
	unsigned char buffer[1];
	
	SET_RESET_PIN(1);
	MDELAY(10);
	SET_RESET_PIN(0);
	MDELAY(10);
	SET_RESET_PIN(1);
	MDELAY(100);
	
	array[0] = 0x00053700;
	dsi_set_cmdq(array, 1, 1);
	MDELAY(10); 
	array[0] = 0x00000100;
	dsi_set_cmdq(array, 1, 1);
	MDELAY(10); 
	
	push_table(lcm_read_id, sizeof(lcm_read_id) / sizeof(struct LCM_setting_table), 1);
	
	read_reg_v2(0x00, buffer, 1);
	device_id = (buffer[0] << 8);
	read_reg_v2(0x01, buffer, 1);
	device_id |= buffer[0];
	
	return (LCM_ID_ILI9806E == device_id) ? 1 : 0;
}

